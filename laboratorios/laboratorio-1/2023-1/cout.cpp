#include <iostream>
using namespace std;

int main(void) {
    cout << "Los streams pueden recibir textos" << endl;
    // Los streams pueden recibir enteros
    cout << 2801 << endl;
    // Los streams pueden recibir reales
    cout << 3.1415 << endl;
    cout << "Un texto seguido de un numero: " << 1999 << endl;
    return 0;
}
